void Translations::translateButtonBox(QDialogButtonBox *buttonBox)
{
    buttonBox->button(QDialogButtonBox::Ok)->setText(RPM::translate(Translations
        ::OK));
    buttonBox->button(QDialogButtonBox::Cancel)->setText(RPM::translate(
        Translations::CANCEL));
}
